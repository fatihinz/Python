print("Lutfen hava durumu gunesli = g , yagmurlu = y , karli = k olarak belirtiniz." )

hava_durumu = input()

if hava_durumu == "g":

    print("Hava cok guzel")

elif hava_durumu == "y":

    print("Islandim!")

elif hava_durumu == "k":

    print("Kar topu oynayalim")

else:

    print("Nasil gidiyor havalar?")